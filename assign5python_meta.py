# It will print the list of fruits 
fruits = ["cherry", "peach", "berries"]
print("Fruits list:", fruits)

# It will print the second fruit of the list
print("Second fruit:", fruits[1])

# It will add a new fruit to the list
fruits.append("grapes")

# It will print the updated list
print("Updated fruits list:", fruits)



